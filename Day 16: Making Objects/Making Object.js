// Day 16 of Javascript! Making Objects.
// The -ish way to create an object.

let master = {
    name : "J.E. B. Vidal",
    monicker : "The -ish!",
    age : 26,
    country : "Philippines"
}

//Rationale above: 'master' is the name of the object. (Variable which is changeable to suit ones preference)
//Rationale below: 'name', 'monicker', 'age', and 'country' are called properties of the object 'master'.

//Accessing the properties of the object 'master' using dot notation.

console.log(master.name);
console.log(master.monicker);
console.log(master.age);
console.log(master.country);

// Rationale below : Use "Name: ", "Monicker: ", "Age: ", and "Country: " to label the output for better readability.

console.log("Name: ",master.name);
console.log("Monicker: ",master.monicker);
console.log("Age: ",master.age);
console.log("Country: ",master.country);


// End of Day 16 of Javascript! Making Objects.