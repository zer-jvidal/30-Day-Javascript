// IEIE - If Else If - Else

let number = 21;

if (number <= 5) {
    console.log("Small"); // if 21 is less than or equal to 5; this will be the output.
} else if (number <= 10) {
    console.log("Medium"); // if 21 is less than or equal to 10; this will be the output.
} else {
    console.log("Large"); // if 21 is greater than 10; this will be the output.
}